package practiceAdvanced.practice05;

public class Q03_Hacim {

    public double hacimHesapla(double a){
        return a * a * a;

    }
    public double hacimHesapla(double a, double b){
       return a * a * b;

    }
    public double hacimHesapla(double a, double b, double c){
        return a * b * c;
    }
}
